# Arithmetic Operators

print(2+2)
print(2-2)
print(2*3)
print(2/2)
print(5//2) #print only integer part of result
print(5%2) 
print(9**2)

#Relational Operators

print(4>2)
print(4<2)
print(4==2)
print(4!=2)
print(4>=2)
print(4<=2)

#logical Operator

print((4>2)and(2!=5))
print((4>2)or(2!=5))
print((4>2)and(2!=5))