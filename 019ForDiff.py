# Here We are Practicing More on for loop

for i in range(1,11):  # Simple Assending Order 
    print(i)
print(" ")

for i in range(1,11,2): #Assending with 2 increment
    print(i)
print(" ")

for i in range(10,0,-1):#Simple Decrement Order
    print(i)
print(" ")

for i in range(10,0,-2):# Decending With 2 decrement.
    print(i)
print(" ")

